async function checkAccess() {
  loadingScreen();

  if (!tg || !tg.initData) {
    applyStoredProgressToState();
    accessDenied("OPEN_FROM_TELEGRAM_REQUIRED");
    return;
  }

  try {
    const response = await fetch(CHECK_ACCESS_URL, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ initData: tg.initData })
    });

    const result = await response.json();

    if (!response.ok || !result.access) {
      console.error("Access denied:", result);
      accessDenied(result.reason || "ACCESS_DENIED");
      return;
    }

    state.access = true;
    state.accessReason = result.reason;
    state.user = result.user;
    state.lesson = result.lesson || null;
    state.progress = result.progress || null;
    state.lastQuizAttempt = result.last_quiz_attempt || null;

    if (state.progress) {
      state.completed.presentation = Boolean(state.progress.presentation_completed);
      state.completed.quiz = Boolean(state.progress.quiz_completed);
      state.completed.books = Boolean(state.progress.books_completed);
      state.completed.homework = Boolean(state.progress.homework_submitted || state.progress.status === "homework_submitted" || state.progress.status === "completed");
    }

    applyStoredProgressToState();
    mainMenu();
  } catch (error) {
    console.error(error);
    accessDenied("CHECK_ACCESS_ERROR");
  }
}

async function saveProgress(event, payload) {
  applyLocalEvent(event, payload || {});

  if (!tg || !tg.initData) {
    return { ok: true, local: true };
  }

  try {
    const response = await fetch(SAVE_PROGRESS_URL, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        initData: tg.initData,
        lessonCode: CURRENT_LESSON_CODE,
        event: event,
        payload: payload || {}
      })
    });

    const result = await response.json();

    if (!response.ok || !result.ok) {
      console.error("Progress save failed:", result);
      return result;
    }

    if (result.progress) {
      state.progress = result.progress;
      state.completed.presentation = Boolean(result.progress.presentation_completed || state.localProgress.presentation_completed_at);
      state.completed.quiz = Boolean(result.progress.quiz_completed || state.localProgress.quiz_completed_at);
      state.completed.books = Boolean(result.progress.books_completed || state.localProgress.books_completed_at);
      state.completed.homework = Boolean(result.progress.homework_submitted || result.progress.status === "homework_submitted" || result.progress.status === "completed" || state.localProgress.homework_submitted_at);
      applyStoredProgressToState();
    }

    return result;
  } catch (error) {
    console.error("Progress save error:", error);
    return { ok: true, local: true, error: "REMOTE_SAVE_FAILED" };
  }
}

async function continueLesson() {
  const p = state.progress;
  if (!p) { await startLesson(); return; }
  if (isHomeworkSubmitted() && !isHomeworkVerified()) { homeworkSubmittedScreen(); return; }
  if (isHomeworkVerified()) { entrepreneurHome(); return; }
  if (p.current_step === "presentation") {
    const savedSlide = Number(p.last_slide_number || 1);
    state.slideIndex = Math.min(Math.max(savedSlide - 1, 0), lessonSlides.length - 1);
    renderLessonSlide();
    return;
  }
  if (p.current_step === "quiz") { quizIntro(); return; }
  if (p.current_step === "books") { startBooks(); return; }
  if (p.current_step === "homework") { homeworkIntro(); return; }
  await startLesson();
}

